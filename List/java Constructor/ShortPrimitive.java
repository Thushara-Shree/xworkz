class ShortPrimitive
{
	public static void main(String[] args){
		short personAge=50;
		System.out.println(personAge);
		short discount=50;
		System.out.println(discount);
		short houseNumber=011;
		System.out.println(houseNumber);
		short count=90;
		System.out.println(count);
		short students=116;
		System.out.println(students);
		
		System.out.println("----re-initilization----");
		personAge=80;
		System.out.println(personAge);
		discount=60;
		System.out.println(discount);
		houseNumber=19;
		System.out.println(houseNumber);
		count=89;
		System.out.println(count);
		students=111;
		System.out.println(students);
	}
}