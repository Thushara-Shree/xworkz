class CharPrimitive{
	public static void main(String[] args){
		char grade='B';
		char bloodGroup='B';
		char block='I';
		char team='C';
		char direcion='N';
		
		System.out.println(grade);
		System.out.println(bloodGroup);
		System.out.println(block);
		System.out.println(team);
		System.out.println(direcion);
		
		System.out.println("----re-initilization----");
	
		grade='A';
		bloodGroup='O';
		block='H';
		team='B';
		direcion='S';
		
		System.out.println(grade);
		System.out.println(bloodGroup);
		System.out.println(block);
		System.out.println(team);
		System.out.println(direcion);
		
	}
}